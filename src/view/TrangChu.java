    private void ShowTable(ArrayList<TaiKhoanModel> list) {
        dtm.setRowCount(0);
        for (TaiKhoanModel tk : list) {
            dtm.addRow(new Object[]{
                tk.getMaTaiKhoan(), tk.getTenTaiKhoan(), tk.getMkTaiKhoan()
            });
        }
    }
       
